public class Operators {
    public static void main(String []args){
int a=5;
int b=4;
System.out.println("Arithmetic operators");
System.out.println(a+b);
System.out.println(a-b);
System.out.println(a*b);
System.out.println(a/b);  

System.out.println("unary operator");
System.out.println(a++);
System.out.println(b++);
System.out.println(a--);
System.out.println(b--);
System.out.println(--a);
}
}
